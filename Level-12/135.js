//Q135. Absolute Difference

function absoluteDiff(a, b) {
    return a > b ? a - b : b - a;
}
console.log(absoluteDiff(5,7))


//-------------------------------------------------------------------------

function absDifference(a,b){
return Math.abs(a-b);
}
console.log(absDifference(10,-5));
console.log(absDifference(-5,5))